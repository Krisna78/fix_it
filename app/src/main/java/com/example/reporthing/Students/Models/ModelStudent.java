package com.example.reporthing.Students.Models;

public class ModelStudent {
    protected String id,username,password;

    public ModelStudent(String id, String username, String password) {
        this.id = id;
        this.username = username;
        this.password = password;
    }

    public String getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }
}
